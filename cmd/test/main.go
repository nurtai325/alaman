func validPhone(phone string) bool {
	if phone == "" {
		return false
	} else if len(phone) != 12 {
		return false
	} else if rune(phone[0]) != '+' {
		return false
	}
	phone = phone[1:]
	for _, r := range phone {
		if r <= 47 || r >= 58 {
			return false
		}
	}
	return true
}

func validPassword(password string) bool {
	if password == "" || len(password) > 72 {
		return false
	} else if len(password) < 8 {
		return false
	}
	return true
}

